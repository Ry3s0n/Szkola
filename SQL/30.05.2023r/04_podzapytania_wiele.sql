-- 1.Utwórz zapytanie, które wyświetli ilości produktów dla poszczególnych kategorii

SELECT *
FROM  (SELECT categoryID, COUNT(*) AS Quantity)
FROM products
GROUP BY categoryID(
    WHERE categoryID>3 AND Quantity BETWEEN 6 AND 9;
)

